    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Connection con = Conexao.AbrirConexao();
        ClinicaDAO sql = new ClinicaDAO(con);

        if (VerificarCampos()) {
            new ErrorMsg().ReceberMsg("Nenhum Campo Pode\n Estar Vazio!");

        } else {
            Clinica a = new Clinica();
            
            a.setNome(lblNome.getText());
            a.setBairro(lblBairro.getText());
            a.setCabecalho(lblCab.getText());
            a.setCep(lblCep.getText());
            a.setCnpj(lblCNPJ.getText());
            a.setNumero(lblN.getText());
            a.setRua(lblRua.getText());
            a.setTelefone(lblTel.getText());
            
            

            boolean resultado = sql.Alterar(a);

            if (resultado) {
                System.out.println("Clínica alterada com sucesso");
                new RightMsg().ReceberMsg("Clínica Alterada com Sucesso!");
                
                new TrocarPanel(pnlPrincipal, new homeAdm());

            } else {
                System.out.println("Erro ao Alterar");
                new ErrorMsg().ReceberMsg("Erro ao Alterar Clínica");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


