    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
         System.out.println("Gerando PDF...");

        Document document = new Document();
        Connection con = Conexao.AbrirConexao();

        ClinicaDAO sqlC = new ClinicaDAO(con);
        
        
        Clinica clinica = sqlC.Capturar();

      

        try {
            PdfWriter.getInstance(document, new FileOutputStream(tipoDocumento+formatadorCompleto.format(dataAtual)+".pdf"));
            document.open();
            
            Paragraph titulo = new Paragraph("Consult√≥rio M√©dico - " + clinica.getNome());
            titulo.setAlignment(Element.ALIGN_CENTER);
            
            
            Paragraph cab = new Paragraph(clinica.getCabecalho());
            cab.setAlignment(Element.ALIGN_CENTER);
            
            document.add(titulo);
            document.add(cab);
            document.add(new Paragraph("\n"));
            document.add(new Paragraph(tipoDocumento+":              Emitido em:"+formatadorSimples.format(dataAtual)));
            document.add(new Paragraph("\n"));
            
            document.add(new Paragraph(iptConteudo.getText()));
            
            document.add(new Paragraph("\n"));
            document.add(new Paragraph("\n"));
            
            document.add(new Paragraph("Doutor: "+nomeMedico));
            
            
            

            
            document.add(new Paragraph("\n"));
            document.add(new Paragraph("\n"));
            document.add(new Paragraph("\n"));
            document.add(new Paragraph("Endere√ßo: "+clinica.getNumero()+", "+clinica.getRua()+", "+clinica.getBairro()));
            document.add(new Paragraph("Telefone: "+clinica.getTelefone()+"        CNPJ: "+clinica.getCnpj()));
            
            
            
            new RightMsg().ReceberMsg("PDF Constru√≠do");
            
            

        } catch (Exception ex) {
            System.out.println("ERROU!!");
        } finally {
            document.close();
            dispose();
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

